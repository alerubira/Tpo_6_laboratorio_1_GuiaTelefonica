package tpo_6_laboratorio_1_guiatelefonica;

/*Un directorio telefónico posee una lista de Clientes de los que interesa conocer su dni, 
nombre, apellido, ciudad y dirección.*/
public class Cliente {
    protected String dni;
    protected String nombre;
    protected String apellido;
    protected String ciudad;
    protected String direccion;
    public Cliente(){
        
    }

    public Cliente(String dni, String nombre, String apellido, String ciudad, String direccion) {
        this.dni = dni;
        this.nombre = nombre;
        this.apellido = apellido;
        this.ciudad = ciudad;
        this.direccion = direccion;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getDni() {
        return dni;
    }

    public String getNombre() {
        return nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public String getCiudad() {
        return ciudad;
    }

    public String getDireccion() {
        return direccion;
    }
    
    
}
